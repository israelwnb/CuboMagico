
public class Vetor {
	double x, y, z;
	
	public Vetor (double a, double b, double c)
	{
		x = a;
		y = b;
		z = c;
	}
	
	public double getX()
	{
		return x;
	}
	
	public double getY()
	{
		return y;
	}
	
	public double getZ()
	{
		return z;
	}
	
	public void setX (double a)
	{
		x = a;
	}
	
	public void setY (double b)
	{
		y = b;
	}
	
	public void setZ (double c)
	{
		z = c;
	}
	
}
